package by.kursy.luschik.chef.model.exception;

public class VegetableProjectException extends Exception {
    public VegetableProjectException() {
        super();
    }
    public VegetableProjectException(String message) {
        super(message);
    }
    public VegetableProjectException(String message, Throwable cause) {
        super(message, cause);
    }
    public VegetableProjectException(Throwable cause) {
        super(cause);
    }

}
